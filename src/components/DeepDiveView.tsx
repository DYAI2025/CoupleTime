import { useTranslation } from 'react-i18next'
import { motion } from 'framer-motion'

interface Card {
  title: string
  content: string
  icon: string
}

interface Section {
  title: string
  cards: Card[]
}

/**
 * Displays detailed guidance cards organized by session phase
 * (Before Session, During Listening, Emergency Situations)
 */
export function DeepDiveView() {
  const { t } = useTranslation()

  // Get sections from i18n
  const beforeSession = t('guidance.deepDive.beforeSession', {
    returnObjects: true,
  }) as Section
  const duringListening = t('guidance.deepDive.duringListening', {
    returnObjects: true,
  }) as Section
  const emergency = t('guidance.deepDive.emergency', {
    returnObjects: true,
  }) as Section

  const sections = [beforeSession, duringListening, emergency]

  return (
    <div className="w-full max-w-6xl mx-auto px-4 py-6">
      <div className="space-y-12">
        {sections.map((section, sectionIndex) => (
          <motion.div
            key={sectionIndex}
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ duration: 0.3, delay: sectionIndex * 0.1 }}
          >
            {/* Section Title */}
            <h2 className="text-2xl font-bold text-gray-900 dark:text-gray-100 mb-6">
              {section.title}
            </h2>

            {/* Cards Grid */}
            {section.cards.length > 0 && (
              <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
                {section.cards.map((card, cardIndex) => (
                  <motion.div
                    key={cardIndex}
                    initial={{ opacity: 0, scale: 0.95 }}
                    animate={{ opacity: 1, scale: 1 }}
                    transition={{
                      duration: 0.2,
                      delay: sectionIndex * 0.1 + cardIndex * 0.05,
                    }}
                    className="bg-white dark:bg-gray-800 rounded-xl p-6 border border-gray-200 dark:border-gray-700 shadow-sm hover:shadow-md transition-shadow"
                  >
                    {/* Icon */}
                    <div className="mb-4 text-indigo-600 dark:text-indigo-400">
                      {getIcon(card.icon)}
                    </div>

                    {/* Card Title */}
                    <h3 className="text-lg font-semibold text-gray-900 dark:text-gray-100 mb-2">
                      {card.title}
                    </h3>

                    {/* Card Content */}
                    <p className="text-gray-700 dark:text-gray-300 text-sm leading-relaxed">
                      {card.content}
                    </p>
                  </motion.div>
                ))}
              </div>
            )}
          </motion.div>
        ))}
      </div>
    </div>
  )
}

